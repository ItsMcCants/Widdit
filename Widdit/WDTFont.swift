import UIKit

extension UIFont {
    class func WDTAgoraRegular(size: CGFloat) -> UIFont {
        return UIFont(name: "PFAgoraSansPro-Regular", size: size)!
    }
    
    class func WDTAgoraMedium(size: CGFloat) -> UIFont {
        return UIFont(name: "PFAgoraSansPro-Medium", size: size)!
    }
}